#include "../philo.h"

int	check_arguments_validation(int argc, char **argv)
{
	int	i;

	if (argc < 5 || argc > 6)
		return (0);
	i = 1;
	while (i < argc)
	{
		if (ft_atoi(argv[i]) <= 0 || ft_atoi(argv[i]) > 2147483647)
			return (0);
		i++;
	}
	return (1);
} 